package bj.powernode.basic.day02;

public class _01_Convert_01 {

	/**
	 * 测试
	 * @param args
	 */
	public static void main( String[] args ) {


		//  两个相同类型的变量进行运算时,结果肯定是同种类型.
		//  两个int类型的值运算结果是 int 类型.

		int i = 1;

		i = i / 10;  // 0.1

		System.out.println( i );







		float f1 = 1.2f;
		float f2 = 123;

		float result = f1 + f2;

		//  "+"作用是 连接字符串
		System.out.println( "compute result:" + result );





	}

}
