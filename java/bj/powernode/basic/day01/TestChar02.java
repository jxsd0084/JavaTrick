package bj.powernode.basic.day01;

public class TestChar02 {

	/**
	 * 测试
	 * @param args
	 */
	public static void main( String[] args ) {

		char c1 = 'c';

		System.out.println( c1 );




		char c2 = 97;   // 97 对应 a

		System.out.println( c2 );





		char c3 = 65;  // 65 对应 A

		System.out.println( c3 );





		char c4 = '\u0043';

		System.out.println( c4 );  // C





	}

}
