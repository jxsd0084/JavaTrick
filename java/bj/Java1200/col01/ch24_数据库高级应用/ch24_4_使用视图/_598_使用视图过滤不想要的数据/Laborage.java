package bj.Java1200.col01.ch24_数据库高级应用.ch24_4_使用视图._598_使用视图过滤不想要的数据;

public class Laborage {

	private int    id;
	private String name;
	private String dept;

	public String getDept() {

		return dept;
	}

	public void setDept( String dept ) {

		this.dept = dept;
	}

	private String laborage;

	public int getId() {

		return id;
	}

	public void setId( int id ) {

		this.id = id;
	}

	public String getName() {

		return name;
	}

	public void setName( String name ) {

		this.name = name;
	}

	public String getLaborage() {

		return laborage;
	}

	public void setLaborage( String laborage ) {

		this.laborage = laborage;
	}

}
