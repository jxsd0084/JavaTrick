package bj.Java1200.col01.ch21_SQL应用.ch21_1_排序和分组函数应用._504_使用GROUP_BY子句实现多表分组统计;

public class CargoBean {

	private String cargoId;
	private String cargoName;
	private String cargoCount;
	private float  cargoPrice;

	public String getCargoId() {

		return cargoId;
	}

	public void setCargoId( String cargoId ) {

		this.cargoId = cargoId;
	}

	public String getCargoName() {

		return cargoName;
	}

	public void setCargoName( String cargoName ) {

		this.cargoName = cargoName;
	}

	public String getCargoCount() {

		return cargoCount;
	}

	public void setCargoCount( String cargoCount ) {

		this.cargoCount = cargoCount;
	}

	public float getCargoPrice() {

		return cargoPrice;
	}

	public void setCargoPrice( float cargoPrice ) {

		this.cargoPrice = cargoPrice;
	}

}
