package bj.Java1200.col01.ch21_SQL应用.ch21_2_聚集函数和日期查询._511_查询与张静同一天入司的员工信息;

public class Emp {

	private int    id;
	private String name;
	private String sex;
	private String dept;
	private String ddate;

	public int getId() {

		return id;
	}

	public void setId( int id ) {

		this.id = id;
	}

	public String getName() {

		return name;
	}

	public void setName( String name ) {

		this.name = name;
	}

	public String getSex() {

		return sex;
	}

	public void setSex( String sex ) {

		this.sex = sex;
	}

	public String getDept() {

		return dept;
	}

	public void setDept( String dept ) {

		this.dept = dept;
	}

	public String getDdate() {

		return ddate;
	}

	public void setDdate( String ddate ) {

		this.ddate = ddate;
	}
}
