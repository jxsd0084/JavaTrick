package bj.Java1200.col01.ch09_编程常用类.ch09_4_Math类的使用._206_反三角函数的使用;

public class MathUtil {

	public static void main( String[] args ) {

		System.out.println( "30°的反正弦值：" + Math.asin( Math.PI / 6 ) );// 计算30°的反正弦
		System.out.println( "30°的反余弦值：" + Math.acos( Math.PI / 6 ) );// 计算30°的反余弦
		System.out.println( "30°的反正切值：" + Math.atan( Math.PI / 6 ) );// 计算30°的反正切
	}
}
