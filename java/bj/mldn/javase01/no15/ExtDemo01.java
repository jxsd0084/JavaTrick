package bj.mldn.javase01.no15;

class Person1 {

	private String name;
	private int    age;

	public void setName( String name ) {

		this.name = name;
	}

	public void setAge( int age ) {

		this.age = age;
	}

	public String getName() {

		return this.name;
	}

	public int getAge() {

		return this.age;
	}
}

class Student1 {

	private String name;
	private int    age;
	private String school;

	public void setName( String name ) {

		this.name = name;
	}

	public void setAge( int age ) {

		this.age = age;
	}

	public void setSchool( String school ) {

		this.school = school;
	}

	public String getName() {

		return this.name;
	}

	public int getAge() {

		return this.age;
	}

	public String getSchool() {

		return this.school;
	}
}
