package bj.mldn.javase01.no02;

public class SimpleDemo05 {    // 声明一个类，类名称的命名规范：所有单词的首字母大写

	/**
	 * 测试
 	 * @param args
	 */
	public static void main( String args[] ) {    // 主方法

		int x = 3;
		int y = 6;

		System.out.println( x & y ); // 2

		System.out.println( x | y ); // 7

		System.out.println( x ^ y ); // 5

	}

}
