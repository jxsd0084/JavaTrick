package bj.mldn.javase01.no40.Gen.gendemo1;

public class Point { // 表示坐标类

	private Object x;
	private Object y;

	public Object getX() {

		return x;
	}

	public void setX( Object x ) {

		this.x = x;
	}

	public Object getY() {

		return y;
	}

	public void setY( Object y ) {

		this.y = y;
	}

}
