public class WrapperDemo03 {

	/**
	 * 测试
	 * @param args
	 */
	public static void main(String args[]) {

		Integer i = 30;    // 自动装箱成Integer
		Float   f = 30.3f;    // 自动装箱成Float
		int     x = i;            // 自动拆箱为int
		float   y = f;        // 自动拆箱为float
	}
}
