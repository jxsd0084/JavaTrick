package bj.mldn.javase02.ch03.no01;

public class DataDemo11 {

	/**
	 * 测试
	 * @param args
	 */
	public static void main(String args[]) {

		float f = 30.3f;        // 浮点型
		int   x = (int) f;      // 强制类型转换

		System.out.println("x = " + x);
		System.out.println("10 / 3 = " + ((float) 10 / 3));    // 执行强制转换

	}
}
